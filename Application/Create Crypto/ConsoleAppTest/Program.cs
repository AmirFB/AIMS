		public static class TAES
		{
			public static byte[] Encrypt(string input)
			{
				Random random = new Random();
				byte[] key = new byte[32];
				byte[] iv = new byte[16];
				List<byte> aes = new List<byte>();
				byte[] cipher;
				List<byte> output = new List<byte>();

				random.NextBytes(key);
				random.NextBytes(iv);

				cipher = Cryptography.AES.Encrypt(Encoding.ASCII.GetBytes(input), key, iv);
				aes.AddRange(key);
				aes.AddRange(iv);
				aes = Complement(aes.ToList());

				for (int i = 0; i < Math.Min(cipher.Length, aes.Count); i++)
				{
					output.Add(cipher[i]);
					output.Add(aes[i]);
				}

				if (cipher.Length > aes.Count)
					for (int i = Math.Min(cipher.Length, aes.Count); i < cipher.Length; i++)
						output.Add(cipher[i]);

				else
					for (int i = Math.Min(cipher.Length, aes.Count); i < aes.Count; i++)
						output.Add(aes[i]);

				return output.ToArray();
			}

			public static string Deccrypt(byte[] input)
			{
				byte[] key = new byte[32];
				byte[] iv = new byte[16];
				List<byte> aes = new List<byte>();
				List<byte> cipher = new List<byte>();
				int min = Math.Min((input.Length - 48) * 2, 96);
				int index = 0;

				while (index < min)
				{
					cipher.Add(input[index++]);
					aes.Add(input[index++]);
				}

				if (input.Length > 96)
					while (index < input.Length)
						cipher.Add(input[index++]);

				else
					while (index < input.Length)
						aes.Add(input[index++]);

				aes = Complement(aes);
				key = aes.GetRange(0, 32).ToArray();
				iv = aes.GetRange(32, 16).ToArray();

				return Cryptography.AES.Decrypt(cipher.ToArray(), key, iv);
			}

			public static List<byte> Complement(List<byte> data)
			{
				data.ForEach(b => b = (byte)(256 - b));
				return data;
			}
		}

		public class ARandom
		{
			private int value = 5, valueo = 101, a = 287, b = 59, c = 117, m = 256, min = 0;

			public ARandom()
			{ }

			public ARandom(int seed)
			{
				value = seed;
			}

			public ARandom(int min, int max)
			{
				this.min = min;
				m = max - min;

				if (a > m)
				{
					a = (int)(a * 256 / m);
					a += a % 2 + 1;
				}

				if (b > m)
				{
					b = (int)(b * 256 / m);
					b += b % 2 + 1;
				}

				if (c > m)
				{
					c = (int)(c * 256 / m);
					c += c % 2;
				}
			}

			public ARandom(int seed, int min, int max)
			{
				value = seed;
				this.min = min;
				m = max - min;

				if (a > m)
				{
					a = (int)(a * 256 / m);
					a += a % 2 + 1;
				}

				if (b > m)
				{
					b = (int)(b * 256 / m);
					b += b % 2 + 1;
				}

				if (c > m)
				{
					c = (int)(c * 256 / m);
					c += c % 2;
				}
			}

			public int Next()
			{
				int temp = value;
				value = (a * value + b * valueo + c) % m;
				valueo = temp;

				return min + value;
			}
		}
